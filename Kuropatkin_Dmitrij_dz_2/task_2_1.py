print(type(15 * 3))  # узнаем тип результата выражения с помощью встроенной функции type
print(type(15 / 3))
print(type(15 // 2))
print(type(15 ** 2))

print(isinstance(15 * 3, int))  # и так же при помощи insistence
print(isinstance(15 / 3, float))
print(isinstance(15 // 2, int))
print(isinstance(15 ** 2, int))
